package OOPS;

public class Addtion {

	
	public void addvalues(int A, int B) {
		
		int C =A+B; 
		System.out.println("Addtion Of C: " +C);
	}
	public void addvalues(int A, int B, int C) {
		
		int D = A+B+C;
		System.out.println("Addtion Of D: " +D);
	}
	
	public double addvalues(double A, double B) {
		
		double D = A+B;
		System.out.println("Double Addtion Is: "+D );
		return D;
	}
}
